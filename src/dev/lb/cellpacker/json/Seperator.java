package dev.lb.cellpacker.json;

public class Seperator {
	private String title;
	private int index;
	
	public Seperator(String title, int index) {
		this.title = title;
		this.index = index;
	}

	public String getTitle() {
		return title;
	}

	public int getIndex() {
		return index;
	}

}
